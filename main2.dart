//2.Write a program to make addition, Subtraction, Multiplication and Division of Two Numbers.
void main(){
  int x=100;
  int y=5;
  var ans;
  // addition using
  ans = (x+y);
  print("this number is a addition : $ans");
  // Subtraction
  ans = (x-y);
  print("this number is a Subtraction : $ans");
  // Multiplication
  ans = (x*y);
  print("this number is a Multiplication : $ans");
  // Division
  ans = (x/y);
  print("this number is a Division : $ans");

}